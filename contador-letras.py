# 1. Solicitar al user que ingrese una palabra.

palabra_ingresada = input("Ingresa una palabra: ")

# 2. Contamos la cantidad de letras que tiene la palabra ingresada.

cantidad_letras = len(palabra_ingresada)

# 3. Mostramos cuantas letras tiene dicha palabra.

print(f'La palabra ingresada: {palabra_ingresada} tiene {cantidad_letras} letras.')